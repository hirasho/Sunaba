inline int getStringSize(const wchar_t* str){
	int l = 0;
	while (str[l] != 0){
		++l;
	}
	return l;
}

inline bool isEqualString(const wchar_t* s0, int l0, const wchar_t* s1, int l1){
	if (l0 != l1){
		return false;
	}
	for (int i = 0; i < l0; ++i){
		if (s0[i] != s1[i]){
			return false;
		}
	}
	return true;
}

//s0はサイズ指定、s1はサイズ不明NULL終端文字列
inline bool isEqualString(const wchar_t* s0, int l0, const wchar_t* s1){
	int i = 0;
	while ((i < l0) && (s1[i])){
		if (s0[i] != s1[i]){
			return false;
		}
		++i;
	}
	return ((i == l0) && (s1[i] == L'\0')); //長さが同じならtrue
}

//両方NULL終端
inline bool isEqualString(const wchar_t* s0, const wchar_t* s1){
	int i = 0;
	while (s0[i] && s1[i]){
		if (s0[i] != s1[i]){
			return false;
		}
		++i;
	}
	return (!(s0[i]) && !(s1[i])); //両方nullなら同じ、そうでなければfalse
}

inline bool isLessString(const wchar_t* s0, int l0, const wchar_t* s1, int l1){
	for (int i = 0; (i < l0) && (i < l1); ++i){
		if (s0[i] < s1[i]){
			return true;
		}else if ( s0[i] > s1[i]){
			return false;
		}
	}
	return (l0 < l1);
}

inline wchar_t toLower(wchar_t c){
	if ((c >= 'A') && (c <= 'Z')){
		c -= 'A';
		c += 'a';
	}
	return c;
}

inline bool checkExtension(const wchar_t* path, const wchar_t* ext){
	int sl = getStringSize(path);
	int el = getStringSize(ext);
	
	int periodPos = sl - 1;
	while (periodPos >= 0){
		if (path[periodPos] == L'.'){
			break;
		}
		--periodPos;
	}
	int l;
	if (periodPos < 0){
		l = 0;
	}else{
		l = sl - periodPos - 1;
	}
	if (l != el){
		return false;
	}
	for (int i = 0; i < l; ++i){
		wchar_t c0 = toLower(path[i + periodPos + 1]);
		wchar_t c1 = toLower(ext[i]);
		if (c0 != c1){
			return false;
		}
	}
	return true;
}

inline bool isAlphabet(wchar_t c){
	bool r = false;
	if ( 
	((c >= L'a') && (c <= L'z')) ||
	((c >= L'A') && (c <= L'Z'))){
		r = true;
	}
	return r;
}

//[?_@$%&a-zA-Z全角0_9]のチェック
inline bool isInName(wchar_t c){ 
	bool r = false;
	if (
	(c == L'@') ||
	(c == L'$') ||
	(c == L'&') ||
	(c == L'?') ||
	(c == L'_') ||
	(c == L'\'') || //y'という名前がつけたくなることはありうるだろうよ "は念のためとっておく
	isAlphabet(c) ||
	((c >= L'0') && (c <= L'9')) ||
	(c >= 0x100)){ //2バイト文字ならなんでもOK
		r = true;
	}
	return r;
}

