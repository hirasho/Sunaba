inline void convertToUnicode(Array<wchar_t>* out, const char* in, int inSize){
	using namespace Internal;
	CharCode cc = guessCharCode(in, inSize);
	if (cc == CHAR_CODE_SJIS){
		convertSjisToUnicode(out, in, inSize);
		if (out->size() == 0){ //失敗した！コード判定しくじったな！仕方ないのでutf8とみなす
			convertUtf8ToUnicode(out, in, inSize);
		}
	}else if (cc == CHAR_CODE_UTF8){
		convertUtf8ToUnicode(out, in, inSize);
	}else if (cc == CHAR_CODE_UTF16L){
		convertUtf16LToUnicode(out, in, inSize);
	}else if (cc == CHAR_CODE_UTF16B){
		convertUtf16BToUnicode(out, in, inSize);
	}else{
		convertUtf8ToUnicode(out, in, inSize); //とりあえずUTF-8とみなす
	}
}
    
inline void convertUnicodeToUtf8( Array<char>* out, const wchar_t* in, int inSize )
{
        // Sunabaは内部が wchar_t に格納された UTF16 で動いている.
        // そのため、ここでは UTF16 , BOM なし でという条件で変換を行う.
        int utf8Length = 0;
        int instrCount = 0;
        const wchar_t* wc = in;
        /* UTF8としての長さをカウント. */
        while( *wc != '\0' && instrCount < inSize ) {
            unsigned code = static_cast<unsigned>( *wc );
            if( code < 0x80 ) {
                utf8Length += 1; // ASCII
            } else if( code < 0x800 ) {
                utf8Length += 2;
            } else {
                if( !(code < 0xD800u || code >= 0xE000u) ) {
                    /* ここは未サポート */
                    printf( "Illegal.\n" );
                }
                utf8Length += 3;
            }
            ++instrCount;
            wc++;
        }
        
        /* UTF8を格納 */
        out->setSize( utf8Length+1 );
        wc = in;
        (*out)[ utf8Length ] = '\0';
        int n = 0;
        instrCount = 0;
        while( *wc != '\0' && instrCount < inSize ) {
            unsigned code = static_cast<unsigned>( *wc );
            if( code < 0x80u ) {
                (*out)[n++] = static_cast<char>( code & 0x7Fu );
            } else if( code < 0x800 ) {
                (*out)[n++] = static_cast<char>( 0xC0u | ((code & 0x7C0u) >> 6) );
                (*out)[n++] = static_cast<char>( 0x80u | (code & 0x3Fu) );
            } else {
                if( !(code < 0xD800u || code >= 0xE000u) ) {
                    printf( "Illegal.\n" );
                }
                (*out)[n++] = static_cast<char>( 0xE0u | ((code & 0xF000u) >> 12));
                (*out)[n++] = static_cast<char>( 0x80u | ((code & 0xFC0u) >> 6 ));
                (*out)[n++] = static_cast<char>( 0x80u | ( code & 0x3fu ) );
            }
            wc++;
            instrCount++;
        }
        (*out)[n] = '\0';
    }
    
    
